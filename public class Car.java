public class Car {

    // Instance variables (attributes)
    private String make;
    private String model;
    private int year;
    private int speed;

    // Constructor
    public Car(String make, String model, int year) {
        this.make = make;
        this.model = model;
        this.year = year;
        this.speed = 0; // car starts at rest
    }
}

